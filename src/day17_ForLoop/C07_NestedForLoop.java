package day17_ForLoop;

public class C07_NestedForLoop {

	public static void main(String[] args) {

// 1 den 4 kadar sayilari yan yana 1 bosluk birakarak yazdirin
		
		
		
		for (int satir = 1; satir <=3; satir++) {
			
			for (int i = 1; i<=3; i++) { 
				System.out.print(satir*i+" ");
				
			}
			System.out.println("");			

		}

	}

}
